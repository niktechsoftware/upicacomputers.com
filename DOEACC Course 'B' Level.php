				  <h1>Project</h1></p>
				  <p>At 'B' level there are two  projects. The first one will be as for 'A' level and to be completed as for 'A'  level. The second project should approximately amount to an effort of 500  man-hours, 300 marks are assigned. The project should be chosen by the  candidate and approved by the DOEACC Society. This should be submitted after  completion of all papers with a fee of Rs. 3000/- and a certificate. DOEACC  Society will constitute a Board of two experts to conduct a viva-voce.</p>
                  
                  <p><h1>Government Recognition</h1></p>
				  <p>The Ministry of Human  Resource Development, Govt. of India vide their notification No: F 2 / 6  /97-TS.IIIa(.) 54 dated 26th September 2000 has recognized the 'B' level  computer examination being conducted by Department of Electronics Accreditation  of Computer Courses (DOEACC) Society for the purpose of employment under the  Central Government where MCA is the desired qualification.</p>
